            int ph=Math.abs(y-y2);
            Robot robot = new Robot();
            
            Rectangle rectangle = new Rectangle(px, py, pw, ph);
            BufferedImage bufferedImage = robot.createScreenCapture(rectangle);
            File file = new File("testing.png");           
